// Igual (==)
// Retorna verdadeiro caso os operandos sejam iguais
'3' == var1
3 == '3'

// N達o igual (!=)
// Retorna verdadeiro caso os operandos n達o sejam iguais
var2 != '3'

// Estritamente igual (===)
// Retorna verdadeiro caso os operandos sejam iguais e do mesmo tipo.
3 === var1

// Estritamente n達o igual (!==)
// Retorna verdadeiro caso os operandos n達o sejam iguais e/ou do mesmo tipo.
var1 !== '3'
3 !== '3'

// Maior que (>)
// Retorna verdadeiro caso o operando da esquerda seja maior que o da direita
var2 > var1
'12' > 2

// Maior que ou igual (>=)
// Retorna verdadeiro caso o operando da esqueda seja maior ou igual ao da direita.
var2 >= var1
var1 >= 3

// Menor que (<)
// Retorna verdadeiro caso operando da esquerda seja meno que o da direita.
var1 < var2
'12' < '2'

// Menor ou igual (<=)
// Retorna verdadeiro caso o operando da esquerda seja menor ou igual ao da direita.
var1 <= var2
var2 <= 5
